//================================================================================================================================================================
//初期設定
//================================================================================================================================================================
HRESULT CSkill_CoolDown::Init(){

	CObject2D::Init();
	SetSize(50.f, 50.f * 0.75f);
	SetPos(640.f, 360.f);
	SetTexture("data\\TEXTURE\\test.jpg");
	SetCollar(0,0,0,100);

	return S_OK;
}

//================================================================================================================================================================
//終了処理
//================================================================================================================================================================
