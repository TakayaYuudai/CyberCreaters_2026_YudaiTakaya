Ctest::Ctest() :CObjectX(0) {
}

//================================================================================================================================================================
//デストラクタ
//================================================================================================================================================================
Ctest::~Ctest() {
}

//================================================================================================================================================================
//初期設定
//================================================================================================================================================================
HRESULT Ctest::Init() {
    CObjectX::Init();
    ModelFilePath("data\\MODEL\\player_base.x");
    
    return S_OK;
}

//================================================================================================================================================================
//終了処理
//================================================================================================================================================================
void Ctest::Uninit() {
    CObjectX::Uninit();
}

//================================================================================================================================================================
//更新処理
