//================================================================================================================================================================
//マクロ定義
//================================================================================================================================================================
#define	BLOCK_WIDE   (17.5f) //ブロックの横半径

//================================================================================================================================================================
//グローバル変数
//================================================================================================================================================================

//================================================================================================================================================================
//コンストラクタ
//================================================================================================================================================================
CIcon_Life::CIcon_Life() :CObject2D(0)
{

}

//================================================================================================================================================================
//デストラクタ
//================================================================================================================================================================
CIcon_Life::~CIcon_Life()
{
}

//================================================================================================================================================================
//初期設定
//================================================================================================================================================================
HRESULT CIcon_Life::Init()
{
	CObject2D::Init();
	SetSize(BLOCK_WIDE, BLOCK_WIDE);
	SetTexture("data\\TEXTURE\\icon_life.png");

	return S_OK;
}

//================================================================================================================================================================
//終了処理
//================================================================================================================================================================
void CIcon_Life::Uninit()
{
	CObject2D::Uninit();
}

//================================================================================================================================================================
//更新処理（全オブジェクト）
//================================================================================================================================================================
void CIcon_Life::Update()
{
	CObject2D::Update();
}

//================================================================================================================================================================
//描画処理（全オブジェクト）
//================================================================================================================================================================
void CIcon_Life::Draw()
{
	CObject2D::Draw();
}

//================================================================================================================================================================
//ブロック生成
//================================================================================================================================================================
CIcon_Life* CIcon_Life::Create(D3DXVECTOR2 pos)
{
	CIcon_Life* pCIcon_Life = new CIcon_Life();
	pCIcon_Life->Init();
	pCIcon_Life->SetPos(pos.x, pos.y);
	CIcon_Circle::Create(pos,23.f,23.f);
	return pCIcon_Life;
}