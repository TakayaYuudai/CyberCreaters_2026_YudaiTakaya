CGage_Back::~CGage_Back()
{

}

//================================================================================================================================================================
//初期設定
//================================================================================================================================================================
HRESULT CGage_Back::Init()
{
	CObject2D::Init();

	SetTexture("data\\TEXTURE\\gage_back.png");

	return S_OK;
}

//================================================================================================================================================================
//終了処理
//================================================================================================================================================================
void CGage_Back::Uninit()
{
	CObject2D::Uninit();
}

//================================================================================================================================================================
//更新処理（全オブジェクト）
//================================================================================================================================================================
void CGage_Back::Update()
{
	CObject2D::Update();
}

//================================================================================================================================================================
//描画処理（全オブジェクト）
//================================================================================================================================================================
void CGage_Back::Draw()
{
	CObject2D::Draw();
}

//================================================================================================================================================================
//ブロック生成
//================================================================================================================================================================
CGage_Back* CGage_Back::Create(D3DXVECTOR2 pos, float x, float y,bool check)
{
	CGage_Back* pCGage_Back = new CGage_Back();
	pCGage_Back->Init();
	pCGage_Back->SetSize(x, y);
	pCGage_Back->SetPos(pos.x, pos.y);
	pCGage_Back->SetDraw(check);
	return pCGage_Back;
}