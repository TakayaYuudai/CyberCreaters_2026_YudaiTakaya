CLight::CLight()
{
	//ライトの情報をクリアする
	ZeroMemory(&m_aLight, sizeof(D3DLIGHT9));
}

//================================================================================================================================================================
//デストラクタ
//================================================================================================================================================================
CLight::~CLight()
{

}

//================================================================================================================================================================
//初期設定
//================================================================================================================================================================
HRESULT CLight::Init()
{
	CRenderer* pRenderer = nullptr;

	pRenderer = CManager::GetInstance()->GetRenderer();

	LPDIRECT3DDEVICE9 RendererDivice = pRenderer->GetDevice();

	D3DXVECTOR3 vecDir[4]; //設定用方向ベクトル

	//ライトの情報をクリアする
	ZeroMemory(&m_aLight, sizeof(D3DLIGHT9));

	//ライトの種類を設定
	m_aLight->Type = D3DLIGHT_DIRECTIONAL;

	//ライトの拡散光を設定
	m_aLight->Diffuse = D3DXCOLOR(1.0f, 1.0f, 1.0f, 1.0f);

	for (int I = 0; I < 4; I++)
	{
		switch (I)
		{
		case 0:
			vecDir[I] = D3DXVECTOR3(0.2f, -0.8f, -0.4f);
			break;

		case 1:
			vecDir[I] = D3DXVECTOR3(0.5f, 0.6f, 0.7f);
			break;

		case 2:
			vecDir[I] = D3DXVECTOR3(1.3f, 0.5f, 2.6f);
			break;

		case 3:
			vecDir[I] = D3DXVECTOR3(1.3f, 0.5f, 2.6f);
			break;
		}
	}

	for (int I = 0; I < 4; I++)
	{
		D3DXVec3Normalize(&vecDir[I], &vecDir[I]);//ベクトルを正規化する

		m_aLight->Direction = vecDir[I];

		//ライトを設定する
		RendererDivice->SetLight(I, m_aLight);
		//ライトを有効にする
		RendererDivice->LightEnable(I, TRUE);
	}

	return S_OK;
}

