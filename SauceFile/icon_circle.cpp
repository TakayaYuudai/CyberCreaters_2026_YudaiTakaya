#include "icon_circle.h"
#include "manager.h"

//================================================================================================================================================================
//コンストラクタ
//================================================================================================================================================================
CIcon_Circle::CIcon_Circle() :CObject2D(0)
{

}

//================================================================================================================================================================
//デストラクタ
//================================================================================================================================================================
CIcon_Circle::~CIcon_Circle()
{
}

//================================================================================================================================================================
//初期設定
//================================================================================================================================================================
HRESULT CIcon_Circle::Init()
{
	CObject2D::Init();
	SetTexture("data\\TEXTURE\\icon_circle.png");

	return S_OK;
}

//================================================================================================================================================================
//終了処理
//================================================================================================================================================================
void CIcon_Circle::Uninit()
{
	CObject2D::Uninit();
}

//================================================================================================================================================================
//更新処理（全オブジェクト）
//================================================================================================================================================================
void CIcon_Circle::Update()
{
	SetRotAnim(0.1f);
	CObject2D::Update();
}

//================================================================================================================================================================
//描画処理（全オブジェクト）
//================================================================================================================================================================
void CIcon_Circle::Draw()
{
	CObject2D::Draw();
}

//================================================================================================================================================================
//ブロック生成
//================================================================================================================================================================
CIcon_Circle* CIcon_Circle::Create(D3DXVECTOR2 pos,float x ,float y)
{
	CIcon_Circle* pCIcon_Circle = new CIcon_Circle();
	pCIcon_Circle->Init();
	pCIcon_Circle->SetSize(x,y);
	pCIcon_Circle->SetPos(pos.x, pos.y);
	return pCIcon_Circle;
}